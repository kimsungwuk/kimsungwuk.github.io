def create_daily_news_posts():
    today = datetime.date.today().isoformat()
    category = "AI 최신뉴스"
    
    news_data_list = get_journalistic_news()
    
    if not news_data_list:
        print("업데이트할 새로운 뉴스 데이터가 없습니다. 실시간 검색을 먼저 수행하세요.")
        return False
    
    data_path = os.path.join(BASE_DIR, "config/posts_data.json")
    
    # 디렉토리가 없으면 생성
    os.makedirs(os.path.dirname(data_path), exist_ok=True)
    
    if os.path.exists(data_path):
        with open(data_path, "r", encoding="utf-8") as f:
            posts_data = json.load(f)
    else:
        posts_data = []

    newly_added = 0
    for item in news_data_list:
        title = item["title"]
        keywords = item.get("keywords", [item["category"], "AI 뉴스", "수익 자동화"])
        
        # 중복 제거 (제목과 날짜가 같으면 기존 것 제거)
        posts_data = [p for p in posts_data if not (p['title'] == title and p['date'] == today)]
        
        posts_data.insert(0, {
            'title': title,
            'date': today,
            'category': category,
            'summary': item["summary"],
            'image_url': item["image_url"],
            'content': item["content"],
            'keywords': keywords
        })
        newly_added += 1
        
    if newly_added > 0:
        with open(data_path, "w", encoding="utf-8") as f:
            json.dump(posts_data, f, indent=4, ensure_ascii=False)
        
        build_script = os.path.join(BASE_DIR, "build_engine.py")
        if os.path.exists(build_script):
            result = subprocess.run(["python3", build_script], capture_output=True, text=True)
            print(f"빌드 출력: {result.stdout}")
            if result.returncode != 0:
                print(f"빌드 에러: {result.stderr}")
        
        # 깃허브 푸시 작업
        try:
            subprocess.run(["git", "-C", BASE_DIR, "add", "."], check=True)
            subprocess.run(["git", "-C", BASE_DIR, "commit", "-m", f"Daily AI News Update {today}"], check=True)
            subprocess.run(["git", "-C", BASE_DIR, "push"], check=True)
            print("깃허브 푸시가 완료되었습니다.")
        except Exception as e:
            print(f"깃 작업 중 오류 발생: {e}")
            
        print(f"총 {newly_added}개의 개별 뉴스 포스팅이 완료되었습니다.")
        return True
    
    return False

if __name__ == "__main__":
    create_daily_news_posts()
