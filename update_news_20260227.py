def create_real_news_posts():
    today = datetime.date.today().isoformat()
    category = "AI 최신뉴스"
    news_data_list = get_real_news_20260227()
    
    data_path = os.path.join(BASE_DIR, "config/posts_data.json")
    with open(data_path, "r", encoding="utf-8") as f:
        posts_data = json.load(f)

    added_titles = []
    for item in news_data_list:
        title = item["title"]
        # Remove existing today's same title to ensure fresh update
        posts_data = [p for p in posts_data if not (p['title'] == title and p['date'] == today)]
        
        posts_data.insert(0, {
            'title': title,
            'date': today,
            'category': category,
            'summary': item["summary"],
            'image_url': item["image_url"],
            'content': item["content"]
        })
        added_titles.append(title)
        
    with open(data_path, "w", encoding="utf-8") as f:
        json.dump(posts_data, f, indent=4, ensure_ascii=False)
    
