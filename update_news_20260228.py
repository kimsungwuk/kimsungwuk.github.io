def create_real_news_posts():
    today = datetime.date.today().isoformat()
    category = "AI 최신뉴스"
    news_data_list = get_real_news_20260228()
    
    data_path = os.path.join(BASE_DIR, "config/posts_data.json")
    if not os.path.exists(data_path):
        print(f"Error: {data_path} not found.")
        return

    with open(data_path, "r", encoding="utf-8") as f:
        posts_data = json.load(f)

    added_count = 0
    for item in news_data_list:
        title = item["title"]
        # Remove existing today's same title to ensure fresh update
        posts_data = [p for p in posts_data if not (p['title'] == title and p['date'] == today)]
        
        posts_data.insert(0, {
            'title': title,
            'date': today,
            'category': category,
            'summary': item["summary"],
            'image_url': item["image_url"],
            'content': item["content"]
        })
        added_count += 1
        
    with open(data_path, "w", encoding="utf-8") as f:
        json.dump(posts_data, f, indent=4, ensure_ascii=False)
    
    # Build blog
    build_script = os.path.join(BASE_DIR, "build_engine.py")
    if os.path.exists(build_script):
        subprocess.run(["python3", build_script], check=True)
        print(f"💰 [성공] 총 {added_count}개의 2월 28일 최신 뉴스 포스팅이 완료되었습니다.")
    else:
        print(f"Error: {build_script} not found.")

if __name__ == "__main__":
    create_real_news_posts()
